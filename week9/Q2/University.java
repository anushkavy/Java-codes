package week9.Q2;

public class University {
    private String Name;
    private int Ranking;
    
    public University(String name, int rank) {
        this.Name = name;
        this.Ranking = rank;
    }

    public String getName() {
        return Name;
    }

    public int getRanking() {
        return Ranking;
    }
    
    

}
